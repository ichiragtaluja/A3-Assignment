      <h1>
        Ford CEO says UAW is ‘holding the deal hostage’ over EV battery plants
      </h1>
      <p>Author: Michael Wayland</p>
    </section>
    <section>
      <img class="article-image-section" src="./../../images/article-images/industry/industry2.webp" alt="ford motor employees with united auto workers" />
    </section>
    <section class="article-text-section">
      <p>
        The United Auto Workers union is holding up negotiations with Ford
        Motor over future electric vehicle battery plants, Ford CEO Jim Farley
        said during a press briefing Friday.
      </p>
      <p>
        “I believe we could have reached a compromise on pay and benefits, but
        so far the UAW is holding the deal hostage over battery plants,” he
        said after the UAW announced it would expand strikes to two additional
        assembly plants — one each for Ford and General Motors.
      </p>
      <p>
        “We have felt from the very beginning, between all the lines of our
        comments, that the original strike was premeditated and that
        everything is taking way too long,” he said. “That actual events are
        predetermined before they happen. It’s been very frustrating.”
        Farley’s public criticism of the union is uncharacteristic for Ford,
        which is historically viewed as the most union-friendly company of the
        Detroit automakers.
      </p>
      <p>
        Farley said the company isn’t “at an impasse” with the union but
        warned that day “could come if this continues.” GM CEO Mary Barra
        echoed much of Farley’s criticisms of Fain and the UAW’s strike
        strategy.
      </p>
