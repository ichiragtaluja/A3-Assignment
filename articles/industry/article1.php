                UAW Mack Trucks union members to join striking Detroit autoworkers on
                picket lines after voting down tentative deal
            </h1>
            <p>Author: Michael Wayland</p>
        </section>
        <section>
            <img class="article-image-section" src="./../../images/article-images/industry/industry1.webp"
                alt="labour union strike in Chicago" />
        </section>
        <section class="article-text-section">
            <p>
                About 3,900 United Auto Workers members with Mack Trucks will go on
                strike Monday after a majority of members rejected a tentative
                agreement reached last week by the union and company. The tentative
                agreement was voted down by 73% of UAW members who voted, the union
                said Sunday night. Workers at facilities in Pennsylvania, Maryland and
                Florida will strike starting at 7 a.m. Monday, UAW announced online.
            </p>
            <p>
                The Mack Trucks workers will add to tens of thousands of other
                striking UAW members, most notably more than 25,000 employees with
                General Motors , Ford Motor and Stellantis . The union launched
                targeted strikes at select facilities against the Detroit automakers
                beginning Sept. 15. The union’s since expanded the strikes at each of
                the automakers. The Mack Trucks deal was viewed as a potential test of
                the willingness of workers to ratify a deal that didn’t meet raised
                expectations set by UAW President Shawn Fain for record contracts for
                hourly pay increases, equal pay for equal work, inflation protection
                and shorter work weeks.
            </p>
            <p>
                One Mack Trucks worker descried the deal as “disgraceful” and an
                “insult” compared to their expectations and what’s currently being
                negotiated by UAW international leaders with the Detroit automakers,
                also known as the Big Three.
            </p>
