      <h1>Why the 'back-up career' is moving further out of reach</h1>
      <p>Author: Megan Carnegie</p>
    </section>
    <section>
      <img class="article-image-section" src="./../../images/article-images/career/career2.webp" alt="Group of factory workers standing" />
    </section>
    <section class="article-text-section">
      <p>
        Chris has been in the animation industry for more than a decade, and
        still gets excited about each week’s assignments. He loves the job,
        but the financial instability is forcing him to consider other
        options. “The industry invests nothing in long-term production
        planning or talent retention,” explains 35-year-old Chris. “So, when
        the production is up, you're kicked to the curb – no severance, no
        nothing.”
      </p>
      <p>
        In the past, he was able to cope with the uncertainty. However, he
        wants children soon, and the fluctuating cash flow is becoming
        problematic. Now, the California-based animator is considering
        pivoting to a ‘back-up career’ in user-experience (UX) design, where
        the market is growing fast, and pay is relatively high. He sees it as
        a largely secure option.
      </p>
      <p>
        These worries resonate with 30-year-old Tiffany, who is desperate for
        change after a decade in the culinary industry. “Even at a respected
        higher-paying bakery, I’m struggling not to feel guilty buying shoes
        or groceries,” explains Tiffany, who lives in Minnesota, US.
        “Animation would be my ideal next career, but the big issue is
        schooling – I just don’t have the finances to back me up.” Taking out
        a loan seems “terrifying”, as Tiffany worries she won’t ever make
        enough to repay the debt as well as cover regular living expenses. For
        Chris’s part, avoiding the burden of education costs is shaping how
        he’s approaching a career pivot. He targeted UX design partly because
        it wouldn’t require full-time education. “As I’m in my mid-thirties
        and about to start a family, I want to avoid going back to school –
        and losing income to do so,” he says.
      </p>
