                Tutoring is one of the most retiree-friendly job opportunities
                available. It allows you to work how and when you want and share
                knowledge gleaned over a lifetime. Platforms such as Wyzant and
                Varsity Tutors make it easy to connect with students, and tutoring
                jobs can be found across virtually all subject areas. What’s more,
                tutoring nowadays takes place both in person and online. "There are
                many online tutoring sites where you can use your experience and
                knowledge to help others learn a new skill or subject – all from the
                comfort of your home,” says Travis Lindemoen, founder of the start-up
                jobs marketplace Enjoy Mondays and managing director at staffing
                agency Nexus IT Group.
